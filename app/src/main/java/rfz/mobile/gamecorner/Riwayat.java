package rfz.mobile.gamecorner;

public class Riwayat {

    public String nama;
    public String sesi;
    public String opsi;
    public String tanggal;
    public String alat;
    public String jumlahAlat;
    public String status;

    public Riwayat(String nama, String sesi, String opsi, String tanggal, String alat, String jumlahAlat, String status) {
        this.nama = nama;
        this.sesi = sesi;
        this.opsi = opsi;
        this.tanggal = tanggal;
        this.alat = alat;
        this.jumlahAlat = jumlahAlat;
        this.status = status;
    }
}
